let weight = 10;
let mass = 6 ;
let earthGravity = 9.81;
let jupiterGravity = 3.71;
let marsGravity = 24.79;
let totalWeight =0;
let counter = 0;


//proceso

function calculateWeightOnPlanet(mass, gravity){
     return mass * gravity;
}



while(mass>=0){
      weight=calculateWeightOnPlanet(mass, earthGravity);
      weight=calculateWeightOnPlanet(mass, marsGravity);
      weight=calculateWeightOnPlanet(mass, jupiterGravity);

      totalWeight = totalWeight +weight
      counter=counter+1;
      mass=mass-1;

}

console.log("the sumTotal is the :" +totalWeight);
console.log("the counter is the :" +counter);
function calculateAverageWeight(totalWeight, counter) {
     return totalWeight / counter;
}
let averageWeight = calculateAverageWeight(totalWeight, counter);
console.log("the average weight is :" +averageWeight);



































































































































































































































































































































































































































